        public bool Save(Screen entry)
        {
            try
            {
                List<Screen> Screen = new List<Screen>();
                Screen.Add(entry);

                List<OutsideBagsCollection> OutsideBagsCollection = entry.OutsideBagsCollectionD;
                if (OutsideBagsCollection == null) OutsideBagsCollection = new List<OutsideBagsCollection>();

                List<BloodCollectionIssues> BloodCollectionIssues = entry.BloodCollectionIssuesD;
                if (BloodCollectionIssues == null) BloodCollectionIssues = new List<BloodCollectionIssues>();

                DBHelper db = new DBHelper();
                db.param = new SqlParameter[] {
                    new SqlParameter("@ErrorMessage", SqlDbType.VarChar, 500),
                    new SqlParameter("@Action", entry.Action),
                    new SqlParameter("@xmlScreen",Screen.ListToXml("Screen")),
                    new SqlParameter("@xmlOutsideBagsCollection",OutsideBagsCollection.ListToXml("OutsideBagsCollection")),
                    new SqlParameter("@xmlBloodCollectionIssues",BloodCollectionIssues.ListToXml("BloodCollectionIssues"))
                };

                db.param[0].Direction = ParameterDirection.Output;
                db.ExecuteSP("BLOODBANK.OutsideBloodCollectionSave");
                this.ErrorMessage = db.param[0].Value.ToString();

                bool isOK = this.ErrorMessage.Split('-')[0] == "100";

                return isOK;
            }
            catch (Exception x)
            {
                this.ErrorMessage = x.Message;
                return false;
            }

        }



        public List<GetSubcenterIssuesByHospList> GetSubcenterIssuesByHosp(int Hospitalid)
