    public class DepartmentSurgeryMappingModel
    {
        public string ErrorMessage { get; set; }

        public List<MainListDepartmentSurgeryMapping> List()
        {
            DBHelper db = new DBHelper();
            DataTable dt = db.ExecuteSPAndReturnDataTable("OT.DepartmentSurgeryMappingMainList");

            List<MainListDepartmentSurgeryMapping> list = new List<MainListDepartmentSurgeryMapping>();
            if (dt.Rows.Count > 0) list = dt.ToList<MainListDepartmentSurgeryMapping>();
            return list;

        }
        public List<Surgery> ShowSelected(int id)
        {
            DBHelper db = new DBHelper();
            db.param = new SqlParameter[] {
                new SqlParameter("@deptid", id)
            };
            DataTable dt = db.ExecuteSPAndReturnDataTable("OT.GetSelectedSurgery");
            List<Surgery> list = new List<Surgery>();
            if (dt.Rows.Count > 0) list = dt.ToList<Surgery>();            

            return list;
        }
        public List<Surgery> FindSurery()
        {
            DBHelper db = new DBHelper();
            DataTable dt = db.ExecuteSPAndReturnDataTable("OT.FindSurgery");

            List<Surgery> list = new List<Surgery>();
            if (dt.Rows.Count > 0) list = dt.ToList<Surgery>();
            return list;
        }

        public bool Save(MainListDepartmentSurgeryMapping entry)
        {
            try
            {
                List<SurgeryStation> SurgeryStation = entry.SurgeryStation;
                if (SurgeryStation == null) SurgeryStation = new List<Models.SurgeryStation>();

                DBHelper db = new DBHelper();
                db.param = new SqlParameter[] {
                    new SqlParameter("@ErrorMessage", SqlDbType.VarChar, 500),
                    new SqlParameter("@Action", entry.Action),
                    new SqlParameter("@xmlSurgeryStation",SurgeryStation.ListToXml("SurgeryStation"))
                };

                db.param[0].Direction = ParameterDirection.Output;
                db.ExecuteSP("OT.DepartmentSurgeryMappingSave");
                this.ErrorMessage = db.param[0].Value.ToString();

                bool isOK = this.ErrorMessage.Split('-')[0] == "100";

                return isOK;
            }
            catch (Exception x)
            {
                this.ErrorMessage = x.Message;
                return false;
            }

        }
 



    }


